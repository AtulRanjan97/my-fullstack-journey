/*
JS is dynamically typed, it means ki humara jo variable hai uska koi type hai nahi, baki dusri language(like c/c++ etc.) me type batana compulsory hai.
    for eg: int x, char ch, float d etc..
    
    in statically typed language, ek baar jo variable jis type ka declare kar diya uske baad us variable me usi type ki value rakh sakte hai.
    for eg: int age = 28;
            age = "atul";  // error (because `age` can only store an integer value)
    
    And this is not true for JS that's why it's called dynamically typed language.
    Eg: let age = 28;
        age = 'atul';
        age = true;     


    In JS, har chiz ke liye ek reference ki memory hogi aur har chiz ke liye ek actual value ki memory hogi, aur value ka jo address hai vo hamesha ek reference ke ander rakha jayega

*/