/*  What is an Odd Loop?

An odd loop is a type of loop where the number of iterations cannot be determined in advance because the termination condition depends on dynamic factors or unpredictable behavior. This contrasts with "normal" loops like for loops, where you typically know how many times the loop will execute before it starts.

Examples of odd loops include:

    1. Waiting for user input (e.g., guessing a number).
    2. Processing until a random event happens.
    3. Iterating until a certain external condition is met.

*/